; This program makes use of registers, labels, and labels which are used to print a character which is 8 bits of
; data wide and 16 bits of data long. In order to print this character, the first step was to find an arithmetic
; expression which would reach the correct FONT_DATA line. This expression was derived by multiplying the 
; hexidecimal value of the designated character (located in the letter asm files) and adding that to the 
; FONT_DATA value. To print the overall character, I looped through each row and each column and used the LDI 
; command and labels to load in the appropriate symbol associated with the x5000 and x5001 values in the 
; letter.asm files.
			
			; R0 is the register that either loads the ZEROVAL, ONEVAL, or ASCII_NL label based on branch condition
			; R1 is the register that counts the number of characters in each row (column counter)
			; R2 is the register that counts total number of rows for the full character to be printed (row counter)
			; R3 is the register that stores the total CHARVAL that needs to be printed
			; R4 is the register that holds the FONT_DATA value
			; R5 is the register that holds the current line value and the corresponding bits that should be printed

			.ORIG x3000

			AND R0, R0, x0000		; Clear the contents of register R0
			AND R1, R1, x0000		; Clear the contents of register R1
			AND R2, R2, x0000		; Clear the contents of register R2
			AND R3, R3, x0000		; Clear the contents of register R3
			AND R4, R4, x0000		; Clear the contents of register R4
			AND R5, R5, x0000		; Clear the contents of register R5
			
			ADD R1, R1, x0008		; Set the column counter to a value of 8

			AND R2, R2, x0000		; Clear R2 which will serve as the counter for the number of lines/rows outputted
			ADD R2, R2, x0008		; Set the counter to a value of 16 by adding 8 twice
			ADD R2, R2, x0008		; Set the counter to a value of 16 by adding 8 twice

			LDI R3, CHARVAL			; Storing the character to be printed into R3

			LEA R4, FONT_DATA		; Loading the FONT_DATA val into R4

			; Multiply R3 by 16 and add this to the existing FONT_DATA value
			ADD R3, R3, R3			; Left-shift R3 effectively multiplying R3 by 2
			ADD R3, R3, R3			; Left-shift R3 effectively multiplying R3 by 4
			ADD R3, R3, R3			; Left-shift R3 effectively multiplying R3 by 8
			ADD R3, R3, R3			; Left-shift R3 effectively multiplying R3 by 16
			ADD R3, R3, R4			; Add the FONT_DATA value stored in R4 with R3

ROWLOOP		
			LDR R5, R3, #0			; Store the line of FONT_DATA which needs to be read into R5
		
COLUMNLOOP	
			ADD R5, R5, #0			; Add R5 with 0 and store it into R5 to retain the line value			
			BRn LABEL   			; Check if the current line value is negative
			LDI R0, ZEROVAL			; If zero OR positive, load the label ZEROVAL and print appropriate character
			BRnzp LABELTWO			; Jump to the OUT statement
LABEL			LDI R0, ONEVAL			; If negative, load the label ONEVAL and print appropriate character
LABELTWO		OUT						

LABEL			LDI R0, ONEVAL			; If negative, load the label ONEVAL and print appropriate character
LABELTWO		OUT						
			ADD R5, R5, R5			; Left shift R5 to read the next significant hex value
			ADD R1, R1, #-1			; Decrement column counter and move to next column
			BRp COLUMNLOOP			; Branch back to the beginning of COLUMNLOOP
			LD R0, ASCII_NL 		; load NewLine ASCII value
			OUT			
			ADD R3, R3, #1 			; Add 1 to R3 and move to the next address/line in FONT_DATA
			ADD R1, R1, x0008		; Set the column counter to a value of 8
			ADD R2, R2, #-1			; Decrement row counter by 1 and move to the next row
			BRp ROWLOOP				; Branch back to the beginning of ROWLOOP

			HALT					; End program execution


ZEROVAL		.FILL x5000				; Label which contains the ASCII value which is represented for the "0" value
ONEVAL 		.FILL x5001				; Label which contains the ASCII value which is represented for the "1" value
CHARVAL		.FILL x5002				; Label which contains the ASCII value for the character needed to be printed
