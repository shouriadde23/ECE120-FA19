; This program makes use of registers, labels, and labels which are used to print a word which is composed of 
; characters that are each 8 bits of data wide and 16 bits of data long. In order to print this word, I built
; on Lab 12 and created a NEXTCHAR loop which would read each character in the word row by row. The next row
; would be read only after a null character was encountered in the first row, which meant that the entire first
; row for all of the characters was read. The next step in program was to develop an arithmetic expression which
; would reach the correct FONT_DATA line. This expression was derived by multiplying the hexidecimal value of the
; designated character (located in the letter asm files) by 16 and adding that to the FONT_DATA value and the row
; counter value. To print the overall overall character, I looped through each row and each column for each 
; character and kept updating the row and column values to finish printing the overall word.  

			
			; R0 is a temporary register that tracks ZEROVAL, ONEVAL, ASCII_NL labels, or row counter
			; R1 is the register that counts the number of characters in each row (column counter)
			; R2 is the register that counts total number of rows for the full character to be printed (row counter)
			; R3 is the register that points to thee CHARVAL that needs to be printed
			; R4 is the register that holds the FONT_DATA value
			; R5 is the register that holds the current character value that needs to be printed to the console
			; R6 holds the appropriate line value stored in R5 and is used to print the appropriate bit values

			.ORIG x3000

			AND R0, R0, #0		; Clear the contents of register R0
			AND R1, R1, #0		; Clear the contents of register R1
			AND R2, R2, #0		; Clear the contents of register R2
			AND R3, R3, #0		; Clear the contents of register R3
			AND R4, R4, #0		; Clear the contents of register R4
			AND R5, R5, #0		; Clear the contents of register R5
			AND R6, R6, #0		; Clear the contents of register R6
			
			ADD R1, R1, #8		; Set the column counter to a value of 8
	
			

			

			LEA R4, FONT_DATA		; Loading the FONT_DATA val into R4

			

ROWLOOP		
			LD R3, CHARVAL			; Storing the character to be printed into R3
			
NEXTCHAR
			AND R1, R1, #0			; Clear the contents of the column counter register
			ADD R1, R1, #8			; Reset the column counter to a value of 8 for the beginning of the NEXTCHAR loop
			LDR R5, R3, #0			; Load the CHARVAL value stored in R3 into R5
			BRz NEWLINE				; If a null character is encountered, skip to the NEWLINE label
			BRnzp #3				; If a null character is not encountered, continue to the left-shifting of R5
NEWLINE		LD R0, ASCII_NL 		; load NewLine ASCII value
			OUT						; OUT to the console
			BRnzp RESET				; Jump to the RESET label 
									
			; Multiply R5 by 16 and add this to the existing FONT_DATA value and the row count value
			ADD R5, R5, R5			; Left-shift R5 effectively multiplying R5 by 2
			ADD R5, R5, R5			; Left-shift R5 effectively multiplying R5 by 4
			ADD R5, R5, R5			; Left-shift R5 effectively multiplying R5 by 8
			ADD R5, R5, R5			; Left-shift R5 effectively multiplying R5 by 16
			ADD R5, R5, R4			; Add the FONT_DATA value stored in R4 with R5
			ADD R5, R5, R2			; Add the row count stored in R2 with R5
			LDR R6, R5, #0			; With R5, load line value into R6 which prints appropriate bit values to screen

COLUMNLOOP	
			ADD R6, R6, #0			; Add R6 with 0 and store it into R6 to retain the line value			
			BRn LABEL   			; Check if the current line value is negative
			LDI R0, ZEROVAL			; If zero OR positive, load the label ZEROVAL and print appropriate character
			BRnzp LABELTWO			; Jump to the OUT statement
LABEL		LDI R0, ONEVAL			; If negative, load the label ONEVAL and print appropriate character
LABELTWO	OUT						
			ADD R6, R6, R6			; Left shift R6 to read the next significant hex value
			ADD R1, R1, #-1			; Decrement column counter and move to next column
			BRp COLUMNLOOP			; Branch back to the beginning of COLUMNLOOP
			ADD R3, R3, #1			; Increment character pointer register R3 by 1, for next character to be printed 
			BRnzp NEXTCHAR
RESET		ADD R2, R2, #1			; Increase row counter by 1
			ADD R0, R2, #-8			; Add R2 with -16 and store that into the temporary register R0
			ADD R0, R0, #-8			; Add R2 with -16 and store that into the temporary register R0
			BRn ROWLOOP				; While R0 is negative loop back to the label ROWLOOP which reads the next row
			
			HALT


ZEROVAL		.FILL x5000				; Label which contains the ASCII value which is represented for the "0" value
ONEVAL 		.FILL x5001				; Label which contains the ASCII value which is represented for the "1" value
CHARVAL		.FILL x5002				; Label which contains the ASCII value for the character needed to be printed
